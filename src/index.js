function Footer() {
  const hour = new Date().getHours();
  const OpensAt = 10;
  const ClosesAt = 22;
  const isOpen = hour <= ClosesAt && hour >= OpensAt;
  console.log(isOpen);
  return (
    <footer className="footer">
      {isOpen ? (
        <Order closeHour={ClosesAt} openHour={OpensAt} />
      ) : (
        <p>We are currently colsed please visit us after {OpensAt}</p>
      )}
    </footer>
  );
}

function Order({ openHour, closeHour }) {
  return (
    <div className="order">
      <p>
        We're open from {openHour} to {closeHour}:00, please come visit us or
        order online
      </p>
      <button className="btn">Order</button>
    </div>
  );
}

